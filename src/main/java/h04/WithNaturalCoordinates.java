package h04;

public interface WithNaturalCoordinates {

    /**
     * @return the x-coordinate
     */
    int getX();

    /**
     * @return the y-coordinate
     */
    int getY();

    /**
     * Set the x-coordinate
     * @param x the x to set
     */
    void setX(int x);

    /**
     * set the y-coordinate
     * @param y the y to set
     */
    void setY(int y);

}
